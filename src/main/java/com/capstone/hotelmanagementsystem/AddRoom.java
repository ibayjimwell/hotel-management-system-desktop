    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        try {
                // Authenticate staff first
                if (!PromptAndCheckPassword()) {
                    return; // Stop if password is incorrect or cancelled
                }

                // Get room number
                String roomNumber = RoomNumberTextField.getText().trim();
                if (roomNumber.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Room number cannot be empty.", "Input Error", JOptionPane.WARNING_MESSAGE);
                    return;
                }

                // Get selected room type
                String selectedType = (String) TypeComboBox.getSelectedItem();
                if (selectedType == null || selectedType.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Please select a room type.", "Input Error", JOptionPane.WARNING_MESSAGE);
                    return;
                }


                // Call database method
                boolean success = Database.AddRoom(roomNumber, selectedType);
                if (success) {
                    JOptionPane.showMessageDialog(this, "Room added successfully.");
                    this.dispose(); // Close the form
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to add room.", "Add Room Failed", JOptionPane.ERROR_MESSAGE);
                }

            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "An error occurred while adding the room.", "Error", JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_AddButtonActionPerformed

