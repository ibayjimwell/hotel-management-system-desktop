   private void loadRoomTypes() {
        ResultSet rs = Database.LoadRoomTypeName();
        try {
            while (rs != null && rs.next()) {
                String type = rs.getString("type");
                TypeComboBox.addItem(type);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading room types.", "Load Error", JOptionPane.ERROR_MESSAGE);
        }
    }
   
   private boolean PromptAndCheckPassword() {
        JPasswordField passwordField = new JPasswordField();
        int option = JOptionPane.showConfirmDialog(
            this,
            passwordField,
            "Enter your password",
            JOptionPane.OK_CANCEL_OPTION,
            JOptionPane.PLAIN_MESSAGE
        );

        if (option == JOptionPane.OK_OPTION) {
            String inputPassword = new String(passwordField.getPassword());
            boolean isCorrect = Database.CheckPassword(this.staff_id, inputPassword);

            if (!isCorrect) {
                JOptionPane.showMessageDialog(this, "Incorrect password.", "Authentication Failed", JOptionPane.ERROR_MESSAGE);
            }

            return isCorrect;
        }

        return false;
    }

   
   

